s = "Python Programming"

print(s.capitalize()) # first letter capital and other lower 
print(len(s))         # length number of string
print(s.count("P"))   # count of P
print(s.casefold())   # all letter lower case
print(s.upper())      # all letter upper case
print(s.lower())      # all letter lower case
print(s.center(25,"*")) # total 25 space, minus string after *
print(s.find("p",2))   #string find P letter of  index[2]
print(s.index("y"))   #index number find of y letter
print(s.isalnum())    #buliun number true/false by string total numilcal number and space included
print(s.isalpha())    #buliun number true/false by string total alphabetic number and space included
print(s.replace("p","y")) #replace by p to y
print(s.title()) #first letter capital of word
print(s.swapcase()) #swapping by capital letter to small letter